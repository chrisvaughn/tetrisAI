def test_detectorist():
    for image, tc in detect_results.items():
        print(f"Image: {image}")
        img = cv2.imread(os.path.join(image_path, image), cv2.IMREAD_GRAYSCALE)
        if img.shape[0] > 240:
            img = cv2.resize(img, (256, 240), interpolation=cv2.INTER_AREA)

        data = np.asarray(img)
        d = Detectorist(data, 10, 5)
        assert d.current_piece.name == tc["current_piece"]
        assert d.next_piece.name == tc["next_piece"]
        if "board" in tc:
            assert np.array_equal(tc["board"], d.board.board)
